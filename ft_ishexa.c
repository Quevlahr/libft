#include "libft.h"

int				ft_ishexa(char c)
{
	if (c == '+' || c == '-' || ft_isdigit(c) == 1 || (c <= 'f' && c >= 'a') ||
			(c <= 'F' && c >= 'A'))
		return (1);
	else
		return (0);
}
